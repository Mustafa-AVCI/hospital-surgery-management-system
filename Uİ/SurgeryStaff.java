    private OperationRoomDAO roomDAO = new OperationRoomDAO();

    private void loadRoomTable() {
        List<OperationRoom> list = roomDAO.getAll();
        DefaultTableModel model = (DefaultTableModel) tblRooms.getModel();
        model.setRowCount(0);

        for (OperationRoom r : list) {
            model.addRow(new Object[]{
                r.getRoomId(),
                r.getRoomCode(),
                r.getDepartmentId(),
                r.getCapacity(),
                r.getStatus()
            });
        }
    }

    private void addRoom() {
        try {
            OperationRoom r = new OperationRoom();
            r.setRoomCode(txtRoomCode.getText());
            r.setDepartmentId(Integer.parseInt(txtDepartmentId.getText()));
            r.setCapacity(Integer.parseInt(txtCapacity.getText()));
            r.setStatus(cmbStatus.getSelectedItem().toString());

            boolean ok = roomDAO.insert(r);
            JOptionPane.showMessageDialog(this, ok ? "Room added successfully!" : "Failed to add room!");

            loadRoomTable();
            clearFields();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void updateRoom() {
        int row = tblRooms.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row!");
            return;
        }

        try {
            OperationRoom r = new OperationRoom();
            r.setRoomId((int) tblRooms.getValueAt(row, 0));
            r.setRoomCode(txtRoomCode.getText());
            r.setDepartmentId(Integer.parseInt(txtDepartmentId.getText()));
            r.setCapacity(Integer.parseInt(txtCapacity.getText()));
            r.setStatus(cmbStatus.getSelectedItem().toString());

            boolean ok = roomDAO.update(r);
            JOptionPane.showMessageDialog(this, ok ? "Updated successfully!!" : "Update failed!");

            loadRoomTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void deleteRoom() {
        int row = tblRooms.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row!");
            return;
        }

        int id = (int) tblRooms.getValueAt(row, 0);

        boolean ok = roomDAO.delete(id);
        JOptionPane.showMessageDialog(this, ok ? "Deleted!" : "Deletion failed!");

        loadRoomTable();
        clearFields();
    }

    private void clearFields() {
        txtRoomCode.setText("");
        txtDepartmentId.setText("");
        txtCapacity.setText("");
        cmbStatus.setSelectedIndex(0);
    }

