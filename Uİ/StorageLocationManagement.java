    private void loadStorageTable() {
        List<StorageLocation> list = slDAO.getAll();
        DefaultTableModel model = (DefaultTableModel) tblStorage.getModel();
        model.setRowCount(0);

        for (StorageLocation s : list) {
            model.addRow(new Object[]{
                s.getLocationId(),
                s.getName(),
                s.getDepartmentId(),
                s.getStatus()
            });
        }
    }

    private void addLocation() {
        try {
            StorageLocation s = new StorageLocation();
            s.setName(txtLocationName.getText());
            s.setDepartmentId(Integer.parseInt(txtDepartment_iD.getText()));
            s.setStatus(cmbStatus.getSelectedItem().toString());

            boolean ok = slDAO.insert(s);
            JOptionPane.showMessageDialog(this, ok ? "Depo eklendi!" : "Ekleme hatası!");

            loadStorageTable();
            clearFields();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hata: " + e.getMessage());
        }
    }

    private void updateLocation() {
        int row = tblStorage.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bir satır seç!");
            return;
        }

        try {
            StorageLocation s = new StorageLocation();
            s.setLocationId(Integer.parseInt(txtLocationId.getText()));
            s.setName(txtLocationName.getText());
            s.setDepartmentId(Integer.parseInt(txtDepartment_iD.getText()));
            s.setStatus(cmbStatus.getSelectedItem().toString());

            boolean ok = slDAO.update(s);
            JOptionPane.showMessageDialog(this, ok ? "Güncellendi!" : "Güncelleme hatası");

            loadStorageTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hata: " + e.getMessage());
        }
    }

    private void deleteLocation() {
        int row = tblStorage.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bir satır seç!");
            return;
        }

        int id = (int) tblStorage.getValueAt(row, 0);
        boolean ok = slDAO.delete(id);

        JOptionPane.showMessageDialog(this, ok ? "Silindi!" : "Silme hatası!");

        loadStorageTable();
        clearFields();
    }

    private void clearFields() {
        txtLocationId.setText("");
        txtLocationName.setText("");
        txtDepartment_iD.setText("");
        cmbStatus.setSelectedIndex(0);
    }

