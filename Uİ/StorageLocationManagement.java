    private void tblStorageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStorageMouseClicked
        // TODO add your handling code here:

        int row = tblStorage.getSelectedRow();
        if (row == -1) {
            return;
        }

        Object v0 = tblStorage.getValueAt(row, 0);
        Object v1 = tblStorage.getValueAt(row, 1);
        Object v2 = tblStorage.getValueAt(row, 2);
        Object v3 = tblStorage.getValueAt(row, 3);

        if (v0 == null) {
            return;
        }

        txtLocationId.setText(v0.toString());
        txtLocationName.setText(v1 != null ? v1.toString() : "");
        txtDepartment_iD.setText(v2 != null ? v2.toString() : "");
        if (v3 != null) {
            String status = v3.toString().trim().toUpperCase();
            cmbStatus.setSelectedItem(status);

        }
    }//GEN-LAST:event_tblStorageMouseClicked

